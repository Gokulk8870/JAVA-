
public class DataTypes {
	public static void main(String[] args) {
		System.out.println("Primitive Data Types");
//		start with lower case
//		Integer
		byte b=10;
		short s=100;
		long l=100000L;
		int i=25;
//		char
		char c='m';
		
//		boolean
		boolean b1=true;
//		floating point
		float f=25.36f; /*7 value after point*/
		double d=78.3456985; /*15 value after point*/
		
		
		System.out.println("Non premtivie data types or reference");
//		start with uppercase
//		string
		String name="gokul";
	}

}
